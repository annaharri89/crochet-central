#pragma mark - Table View

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [self.dataController countOfMasterList];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"BasicsCell" forIndexPath:indexPath];
    CrochetBasics *basicsAtIndex = [self.dataController objectInListAtIndex:indexPath.row];
    [[cell textLabel] setText:basicsAtIndex.name];

    return cell;
}

